function buildContent(template: string, data: WizardFormData): string {
  const standardsStr =
    data.standards.length > 0
      ? data.standards
          .map((s) => {
            const labels: Record<string, string> = {
              GOST57580: 'ГОСТ Р 57580',
              FSTEC239: 'Приказ ФСТЭК №239',
              FSTEC17: 'Приказ ФСТЭК №17',
              FSTEC21: 'Приказ ФСТЭК №21',
              FZ152: 'ФЗ-152 «О персональных данных»',
              FZ187: 'ФЗ-187 «О безопасности КИИ»',
              FSTEC_MD: 'Методика оценки угроз ФСТЭК (2021)',
              ISO27001: 'ISO/IEC 27001:2022',
              NIST: 'NIST SP 800-53',
              GDPR: 'GDPR',
              'PCI-DSS': 'PCI DSS v4.0',
              CIS: 'CIS Controls v8',
            };
            return labels[s] || s;
          })
          .join(', ')
      : 'применимых стандартов и лучших практик';

  return template
    .replace(/{org}/g, data.organizationName || 'Организации')
    .replace(/{protectionObject}/g, data.protectionObject || 'информационной системы')
    .replace(/{dataCategory}/g, data.dataCategory || 'конфиденциальной информации')
    .replace(/{standards}/g, standardsStr);
}

export const mockApi = {
  async generateDocument(form: WizardFormData): Promise<GenerationResult> {
    await delay(1500);

    if (!form.documentType) throw new Error('Тип документа обязателен.');

    const docType = form.documentType;
    const rawSections = SECTION_TEMPLATES[docType] ?? SECTION_TEMPLATES['policy'];

    const sections: GeneratedSection[] = rawSections.map((s) => {
      const content = buildContent(s.content, form);
      return {
        id: uid(),
        title: s.title,
        content,
        wordCount: content.split(/\s+/).length,
      };
    });

    const allSources: Source[] = [];
    if (form.standards.length > 0) {
      form.standards.forEach((std) => {
        const pool = SOURCE_POOL[std];
        if (pool) allSources.push(...pool);
      });
    } else {
      Object.values(SOURCE_POOL)
        .slice(0, 2)
        .forEach((pool) => allSources.push(...pool.slice(0, 1)));
    }

    const totalWords = sections.reduce((acc, s) => acc + s.wordCount, 0);
    const docTitle = form.title || DOCUMENT_TITLES[docType];

    return {
      id: uid(),
      documentType: docType,
      title: form.organizationName ? `${docTitle} — ${form.organizationName}` : docTitle,
      sections,
      sources: allSources,
      standards: form.standards,
      organizationName: form.organizationName,
      generatedAt: new Date(),
      wordCount: totalWords,
      pageEstimate: Math.max(1, Math.ceil(totalWords / 300)),
    };
  },

  async getKnowledgeGraph(): Promise<KnowledgeGraphData> {
    await delay(300);
    return KNOWLEDGE_GRAPH_DATA;
  },
};

class RegulatoryClassifier:
    """Классификатор нормативных профилей организации"""
    
    def __init__(self):
        self.profiles = REGULATORY_PROFILES
    
    def classify(
        self, 
        organization_name: str, 
        context: Optional[Dict] = None
    ) -> tuple[RegulatoryRegime, RegulatoryProfile]:
        """
        Определяет нормативный профиль по названию организации и контексту.
        
        :param organization_name: Название организации
        :param context: Дополнительный контекст (is_kii_subject, is_gis_operator и т.д.)
        :return: (RegulatoryRegime, RegulatoryProfile)
        """
        ctx = context or {}
        org_lower = organization_name.lower()
        
        # ПРИОРИТЕТ: Сначала проверяем специальные статусы (КИИ, ГИС)
        # Если организация — субъект КИИ, это главный профиль
        if ctx.get("is_kii_subject", False) or any(re.search(p, org_lower) for p in [
            r"энерг", r"электросет", r"теплосет", r"теплоэнерг",
            r"генераци", r"распределит.*сет", r"кии", r"асу тп", r"scada"
        ]):
            return RegulatoryRegime.KII_OPERATOR, self.profiles[RegulatoryRegime.KII_OPERATOR]
        
        # Если организация — оператор ГИС
        if ctx.get("is_gis_operator", False) or any(re.search(p, org_lower) for p in [
            r"администрац", r"министерств", r"ведомств", r"мэри",
            r"правительств", r"государствен", r"муниципал", r"гис", r"егисз"
        ]):
            return RegulatoryRegime.GIS_OPERATOR, self.profiles[RegulatoryRegime.GIS_OPERATOR]
        
        # ОТРАСЛЕВАЯ КЛАССИФИКАЦИЯ (если нет специальных статусов)
        
        # === МЕДИЦИНСКИЕ ОРГАНИЗАЦИИ ===
        if any(re.search(p, org_lower) for p in [
            r"буз", r"миац", r"больниц", r"поликлиник", 
            r"медцентр", r"диагностическ", r"стоматолог", r"здравоох"
        ]):
            return RegulatoryRegime.MEDICAL_PD, self.profiles[RegulatoryRegime.MEDICAL_PD]
        
        # === ОБРАЗОВАТЕЛЬНЫЕ УЧРЕЖДЕНИЯ ===
        if any(re.search(p, org_lower) for p in [
            r"университет", r"вуз", r"институт", r"академ",
            r"колледж", r"техникум", r"школ", r"лицей",
            r"гимназ", r"училищ", r"образован"
        ]):
            return RegulatoryRegime.EDUCATION_PD, self.profiles[RegulatoryRegime.EDUCATION_PD]
        
        # === ФИНАНСОВЫЕ ОРГАНИЗАЦИИ ===
        if any(re.search(p, org_lower) for p in [
            r"банк", r"нко", r"кредит", r"страхов", r"финанс",
            r"платеж", r"платёж", r"микрофинанс", r"ломбард"
        ]):
            return RegulatoryRegime.FINANCIAL_PD, self.profiles[RegulatoryRegime.FINANCIAL_PD]
        
        # === ПО УМОЛЧАНИЮ ===
        logger.info(f"Не удалось определить профиль для '{organization_name}', используется GENERIC")
        return RegulatoryRegime.GENERIC, self.profiles[RegulatoryRegime.GENERIC]
    
    def get_applicable_standards(
        self, 
        profile: RegulatoryProfile, 
        context: Optional[Dict] = None
    ) -> List[str]:
        """
        Возвращает итоговый список применимых стандартов с учётом условий.
        
        :param profile: Нормативный профиль
        :param context: Контекст для проверки условий
        :return: Список ключей стандартов
        """
        ctx = context or {}
        standards = set(profile.mandatory_standards)
        standards.update(profile.recommended_standards)
        
        for std_key, condition in profile.conditional_standards.items():
            if condition(ctx):
                standards.add(std_key)
        
        return sorted(list(standards))
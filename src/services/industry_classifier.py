from typing import Dict, List, Optional, Callable
from dataclasses import dataclass, field

logger = logging.getLogger(__name__)


class RegulatoryRegime(Enum):
    """Нормативные режимы РФ по информационной безопасности"""
    
    # === БАЗОВЫЕ (применяются почти всегда) ===
    PD_OPERATOR = "pd_operator"  # Оператор персональных данных (152-ФЗ)
    
    # === ОТРАСЛЕВЫЕ СПЕЦИФИЧНЫЕ ===
    MEDICAL_PD = "medical_pd"  # Медицина + ПДн (152-ФЗ + Приказ Минздрава №956н)
    EDUCATION_PD = "education_pd"  # Образование + ПДн (152-ФЗ + ФИС ФРДО)
    KII_OPERATOR = "kii_operator"  # Субъект КИИ (187-ФЗ + ФСТЭК №239)
    GIS_OPERATOR = "gis_operator"  # Оператор ГИС (149-ФЗ + ФСТЭК №17)
    FINANCIAL_PD = "financial_pd"  # Финансы + ПДн (152-ФЗ + Стандарты ЦБ)
    
    # === РЕЗЕРВ ===
    GENERIC = "generic"  # Универсальный профиль (только 152-ФЗ)


@dataclass
class RegulatoryProfile:
    """Профиль нормативных требований для организации"""
    
    regime: RegulatoryRegime
    name: str  # Человекочитаемое название
    
    # === НОРМАТИВНАЯ БАЗА ===
    mandatory_standards: List[str]  # Всегда применяются
    recommended_standards: List[str]  # Рекомендуются, но не обязательны
    conditional_standards: Dict[str, Callable[[Dict], bool]]  # Применяются при условиях
    
    # === КОНТЕКСТ ДЛЯ ГЕНЕРАЦИИ ===
    key_terms: List[str]  # Отраслевая терминология
    protected_data: List[str]  # Специфичные защищаемые данные
    typical_roles: List[str]  # Типичные роли пользователей
    typical_systems: List[str]  # Отраслевые информационные системы
    specific_risks: List[str]  # Специфичные риски
    example_phrases: Dict[str, str] = field(default_factory=dict)

    # Отраслевые векторы (опциональные рекомендации для мягкой адаптации)
    industry_vectors: Dict[str, List[str]] = field(default_factory=dict)
    """
    Формат:
    {
        "key_terms": ["термин1", "термин2"],
        "protected_data": ["данные1", "данные2"],
        "typical_roles": ["роль1", "роль2"],
        "typical_systems": ["система1", "система2"],
    }
    Используется в ArchitectAgent для формирования блока рекомендаций.
    """

    # === ДОПОЛНИТЕЛЬНО ===
    description: str = ""
    notes: List[str] = field(default_factory=list)


